// Format phone number for display
function formatPhoneNumber(phone: string | undefined): string {
  if (!phone) return ''
  // Simple formatting: +XX (XX) XXXXX-XXXX
  const cleaned = phone.replace(/\D/g, '')
  if (cleaned.length === 13) {
    return `+${cleaned.slice(0, 2)} (${cleaned.slice(2, 4)}) ${cleaned.slice(4, 9)}-${cleaned.slice(9)}`
  }
  return phone
}

function ChannelNode(props: NodeProps<ChannelNodeData>) {
  const { data } = props
  const config = channelConfig[data.channelType] || defaultConfig
  const isGlowing = data.isGlowing ?? false
  const isFading = data.isFading ?? false

  // Determine subtitle based on channel type
  let subtitle: string | null = null
  if (data.channelType === 'whatsapp' && data.phoneNumber) {
    subtitle = formatPhoneNumber(data.phoneNumber)
  } else if (data.channelType === 'telegram' && data.botUsername) {
    subtitle = `@${data.botUsername}`
  }

  return (
    <BaseNode {...props} className={isGlowing && !isFading ? 'channel-node-active' : isFading ? 'channel-node-fading' : ''}>
      <div className="flex items-center gap-2">
        <div className={`w-8 h-8 rounded-lg ${config.colorClass} flex items-center justify-center relative`}>
          {config.icon}
          {/* Status indicator dot */}
          {data.status && (
            <span
              className={`absolute -top-0.5 -right-0.5 w-2.5 h-2.5 rounded-full border-2 border-tsushin-surface ${statusColors[data.status]}`}
              title={data.status}
            />
          )}
        </div>
        <div className="flex flex-col">
          <span className="text-sm text-white capitalize">{data.label}</span>
          {subtitle && (
            <span className="text-xs text-tsushin-slate truncate max-w-[120px]">{subtitle}</span>
          )}
        </div>
      </div>
    </BaseNode>
  )
}

export default memo(ChannelNode)
